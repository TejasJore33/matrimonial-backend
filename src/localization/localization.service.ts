  constructor(private prisma: PrismaService) {}

  async getTranslations(language: string = 'en'): Promise<{ [key: string]: string }> {
    const result: { [key: string]: string } = {};

    Object.keys(this.translations).forEach(key => {
      result[key] = this.translations[key][language] || this.translations[key]['en'];
    });

    return result;
  }

  async translate(key: string, language: string = 'en'): Promise<string> {
    if (this.translations[key]) {
      return this.translations[key][language] || this.translations[key]['en'] || key;
    }
    return key;
  }


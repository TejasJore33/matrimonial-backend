  async convertCurrency(amount: number, fromCurrency: string, toCurrency: string): Promise<number> {
    // In production, use a currency conversion API (e.g., ExchangeRate-API, Fixer.io)
    // For now, return mock conversion rates

    const exchangeRates: { [key: string]: number } = {
      'USD': 1,
      'INR': 83.0,
      'EUR': 0.92,
      'GBP': 0.79,
      'AED': 3.67,
      'SAR': 3.75,
    };

    const fromRate = exchangeRates[fromCurrency.toUpperCase()] || 1;
    const toRate = exchangeRates[toCurrency.toUpperCase()] || 1;

    return (amount / fromRate) * toRate;
  }

  async getUserLanguage(userId: string): Promise<string> {
    const user = await this.prisma.user.findUnique({
      where: { id: userId },
      select: { preferredLanguage: true },
    });

    return user?.preferredLanguage || 'en';
  }

  async setUserLanguage(userId: string, language: string): Promise<void> {
    await this.prisma.user.update({
      where: { id: userId },
      data: { preferredLanguage: language },
    });
  }
